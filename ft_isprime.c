int		ft_isprime(unsigned long int n)
{
	unsigned long int	i;

	if (n < 4)
		return (n < 2 ? 0 : 1);
	i = 2;
	while (i <= n / i)
		if (n % i)
			i++;
		else
			return (0);
	return (1);
}
